    <p style="margin-left: 50px;" > В <b>Forza Horizon 4</b> у каждого автомобиля своё управление, отличие двигателя, подвески, аэродинамики, развесовки автомобиля и т. д.
        Но сделан во всех автомобилях свой баланс, так как выбрав одну марку, есть большой выбор среди моделей одной марки можно заметить приемственность поведения автомобиля, на столько они продумали свою игру. Возьмём в пример автомобили ferrari, на разгоне автомобиль совершенно неуправляем, но уже на третей передачи с помощью аэродинамики и хороших шин автомобили этой марки словно прилипают к дороге, это если рассматривать автомобили этой марки на заднем приводе и предназначенные для трека. У более низкого класса управление более валкое, но в поворот входят лучше чем некоторые конкуренты.
        Но в этой работе я буду рассматривать автомобили koenigsegg.
        С самого начала эта марка мне приглянулась, особенно koenigsegg regera из-за своего дизайна, управления, большой мощности и из-за великолепного электродвигателя, который на заднем приводе очень быстро набирает скорость от 100 и до 350 км/ч.
        Также среди марок можно выявить отличие koenigsegg от остальных.
        Их всего в игре 7 штук и у всех них есть общие черты, которые можно перечислить. Конечно же жёсткая подвеска, мощный двигатель (нету ни одного автомобиля этой марки, у которого лошадиных сил меньше 1000), управление (так же в начале нужно выдержать долгие буксы до 80 км/ч, далее постепенно давить на педаль газа, от 150 км/ч на большинства автомобилей этой марки можно выходить из поворота, не боясь, что тебя снесёт и ты отправишься в дрифт), быстрый набор скорости (если хорошо отрегулировать коробку передач, то можно достигнуть хорошего балансе между максимальной скорости и разгоном).
        Здесь мы рассмотрим все автомобили этой марки, находящихся в игре Forza Horizon 4.
